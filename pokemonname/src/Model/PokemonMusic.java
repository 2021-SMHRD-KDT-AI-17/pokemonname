package Model;

public class PokemonMusic {
	
	private String path;
	private String  title;
	public PokemonMusic(String path, String title) {
		super();
		this.path = path;
		this.title = title;
	}
	public String getPath() {
		return path;
	}
	public String getTitle() {
		return title;
	}
	

}
