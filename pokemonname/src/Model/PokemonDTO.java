package Model;

public class PokemonDTO {

	private String name;
	
	private int num;
	
	private String hint;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
	
	public String getHint() {
		return hint;
	}

	public void setHint(String hint) {
		this.hint = hint;
	}

	public PokemonDTO() {
		
	}
	
}
